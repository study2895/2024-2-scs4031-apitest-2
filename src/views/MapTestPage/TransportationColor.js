// 함수 내보내기
export function getBusRouteColor(routeType) {
  return busRouteColors[routeType] || busRouteColors[1] // 기본값은 일반 버스
}

export function getBusCongestionColor(congestionType) {
  return busCongestionColors[congestionType] || busCongestionColors['-1'] // 기본값은 데이터 없음
}

export function getSubwayColor(lineType) {
  return subwayColors[lineType] || subwayColors[1] // 기본값은 수도권 1호선
}
